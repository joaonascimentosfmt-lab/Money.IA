function getCustomProducts() {
  try {
    return JSON.parse(localStorage.getItem('mone_custom_products') || '[]');
  } catch(e) { return []; }
}

function saveCustomProducts(products) {
  try {
    localStorage.setItem('mone_custom_products', JSON.stringify(products));
  } catch(e) {}
}

function getAllProducts() {
  return PRODUCTS.concat(getCustomProducts());
}

function getProductDisplay(product) {
  return getProductIcon(product.category);
}
